package com.my.chat20.vo;

public class Follow {

	
	private int follow_idx=0;
	private int fan_idx=0;
	private int star_idx=0;
	private String reg_date=null;
	public int getFollow_idx() {
		return follow_idx;
	}
	public void setFollow_idx(int follow_idx) {
		this.follow_idx = follow_idx;
	}
	public int getFan_idx() {
		return fan_idx;
	}
	public void setFan_idx(int fan_idx) {
		this.fan_idx = fan_idx;
	}
	public int getStar_idx() {
		return star_idx;
	}
	public void setStar_idx(int star_idx) {
		this.star_idx = star_idx;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	
	
}
